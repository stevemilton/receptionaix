import { createClient } from '@/lib/supabase/server';
import { redirect } from 'next/navigation';
import Link from 'next/link';

interface MerchantData {
  id: string;
  business_name: string;
  onboarding_completed: boolean;
}

export default async function DashboardLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  const supabase = await createClient();
  const { data: { user } } = await supabase.auth.getUser();

  if (!user) {
    redirect('/auth/login');
  }

  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  const { data: merchant } = await (supabase as any)
    .from('merchants')
    .select('id, business_name, onboarding_completed')
    .eq('id', user.id)
    .single();

  if (!merchant?.onboarding_completed) {
    redirect('/onboarding');
  }

  const navItems = [
    { href: '/dashboard', label: 'Overview', icon: HomeIcon },
    { href: '/dashboard/calls', label: 'Calls', icon: PhoneIcon },
    { href: '/dashboard/appointments', label: 'Appointments', icon: CalendarIcon },
    { href: '/dashboard/customers', label: 'Customers', icon: UsersIcon },
    { href: '/dashboard/knowledge', label: 'Knowledge Base', icon: BookIcon },
    { href: '/dashboard/billing', label: 'Billing', icon: CreditCardIcon },
    { href: '/dashboard/settings', label: 'Settings', icon: SettingsIcon },
  ];

  return (
    <div className="min-h-screen bg-gray-50 flex">
      {/* Sidebar */}
      <aside className="w-64 bg-white border-r border-gray-200 flex flex-col">
        <div className="p-6 border-b border-gray-200">
          <Link href="/dashboard" className="text-xl font-bold text-primary-600">
            ReceptionAI
          </Link>
          <p className="text-sm text-gray-500 mt-1 truncate">{merchant.business_name}</p>
        </div>

        <nav className="flex-1 p-4 space-y-1">
          {navItems.map((item) => (
            <NavLink key={item.href} href={item.href} icon={item.icon}>
              {item.label}
            </NavLink>
          ))}
        </nav>

        <div className="p-4 border-t border-gray-200">
          <div className="flex items-center gap-3 mb-3">
            <div className="w-8 h-8 bg-primary-100 rounded-full flex items-center justify-center">
              <span className="text-sm font-medium text-primary-600">
                {user.email?.[0]?.toUpperCase()}
              </span>
            </div>
            <div className="flex-1 min-w-0">
              <p className="text-sm font-medium text-gray-900 truncate">{user.email}</p>
            </div>
          </div>
          <form action="/api/auth/signout" method="POST">
            <button
              type="submit"
              className="w-full text-left text-sm text-gray-600 hover:text-gray-900 px-3 py-2 rounded-lg hover:bg-gray-100"
            >
              Sign out
            </button>
          </form>
        </div>
      </aside>

      {/* Main content */}
      <main className="flex-1 overflow-auto">
        {children}
      </main>
    </div>
  );
}

function NavLink({
  href,
  icon: Icon,
  children,
}: {
  href: string;
  icon: React.ComponentType<{ className?: string }>;
  children: React.ReactNode;
}) {
  return (
    <Link
      href={href}
      className="flex items-center gap-3 px-3 py-2 text-sm font-medium text-gray-700 rounded-lg hover:bg-gray-100 hover:text-gray-900 transition-colors"
    >
      <Icon className="w-5 h-5" />
      {children}
    </Link>
  );
}

